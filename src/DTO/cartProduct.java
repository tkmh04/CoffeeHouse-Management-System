package DTO;

import java.util.ArrayList;
import java.util.List;

/**
 *
 * @author asus
 */
public class cartProduct {
    private int id;
    private String name;
    private String toppings;
    private double price;
    private int quant;
    private String size;

    public cartProduct(int id,String name, String toppings, double price, int quant,String size) {
        this.size=size;
        this.id = id;
        this.name = name;
        this.toppings = toppings;
        this.price = price;
        this.quant = quant;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getToppings() {
        return toppings;
    }

    public void setToppings(String toppings) {
        this.toppings = toppings;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public int getQuant() {
        return quant;
    }

    public void setQuant(int quant) {
        this.quant = quant;
    }
    
    
}
